@property (strong) TournamentsModel* model;
@end

@implementation AttendedTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.model = [TournamentsModel sharedModel];
    [[NSNotificationCenter defaultCenter] addObserver:self selector:@selector(reload) name:@"reload_attended_TVC" object:nil];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
